let selectedAccount
let nftContract
let isInitialized = false

export const initContract = async () => {
  const provider = window.ethereum

  if (typeof provider !== 'undefined') {
    await provider
      .request({ method: 'eth_requestAccounts' })
      .then((accounts) => {
        selectedAccount = accounts[0]
        console.log(`Selected account is ${selectedAccount}`)
      })
      .catch((err) => {
        console.log(err)
      })
    window.ethereum.on('accountsChanged', function (accounts) {
      selectedAccount = accounts[0]
      console.log(`Selected account changed to ${selectedAccount}`)
    })
  }
  const web3 = new Web3(provider)
  // const networkId = await web3.eth.net.getId()
  // nftContract = new web3.eth.Contract(NFTContractBuild.abi, NFTContractBuild.networks[networkId].address)
  nftContract = new web3.eth.Contract(abi, '0x0114E7dE85e905DeD98f999bA4eAA4e5FCa622A2')
  isInitialized = true
}

export const mintToken = async (url) => {
  if (!isInitialized) {
    await initContract()
  }
  console.log(selectedAccount)
  return nftContract.methods
    .safeMint(selectedAccount, url)
    .send({ from: selectedAccount })
}

